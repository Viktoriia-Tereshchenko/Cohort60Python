# Каждый символ в строке имеет порядковый номер,
# называемый индексом, нумерация начинается с 0 слева направо
# мы можем обращаться к символу по индексу

word = "Python"
print(word[0]) # Вывод P - самый левый (первый) символ строки
print(word[4]) # Вывод o - (пятый символ)
print(word[1]) # Вывод y - символ с индексом 1, но по порядку он 2-й слева
print('============')

# Отрицательные индексы
print(word[-1])  # Вывод n - последний (самый правый) символ в строке
print(word[-2])  # Вывод o - второй справа (предпоследний) символ в строке

# Конкатенация (соединение) строк
# Операция + используется только с одинаковыми типами данных
# для чисел + выполняется сложение
# для строк + выполняет конкатенацию

fist_name = "Иван"
last_name = "Петров"
full_name = fist_name + " " + last_name
print(full_name)  # Иван Петров

# Если соединить строку и число
age = 25
message = "Мне " + str(age) + " лет."
print(message)

