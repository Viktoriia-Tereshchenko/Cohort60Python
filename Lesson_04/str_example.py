# У n-го по порядку символа будет индекс n-1

# Срезы строк (выделение подстроки)
text = "Программирование"

# Первый индекс включительно, а второй не включительно !!!
print(text[0:6]) # Вывод Програ (символы с 0 по 5-й)

sub_string = text[0:2]
print("sub:", sub_string) # Вывод Пр - выделена подстрока начиная с индекса 0 (вкл) до символа 2 (не вкл)

print(text[:6])  # Вывод Прога (символы с 0 по 5)
print(text[6:])  # Вывод ММИРОВАНИЕ начиная с 6 (вкл) и до конца строки

print(text[6:10])  # Вывод ммир -> [6, 10) -> 6 (вкл) дл (10-1) включительно

